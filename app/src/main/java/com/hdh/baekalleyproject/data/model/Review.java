package com.hdh.baekalleyproject.data.model;

public class Review {
    private String userImageURL;
    private String userName;
    private String reviewRegisteredTime;
    private String userFoodEaten;
    private String userEvaluation;
    private String reviewTitle;
    private String reviewContent;
    private String reviewLikeCount;
    private String reviewComments;

    public String getUserImageURL() {
        return userImageURL;
    }

    public String getUserName() {
        return userName;
    }

    public String getReviewRegisteredTime() {
        return reviewRegisteredTime;
    }

    public String getUserFoodEaten() {
        return userFoodEaten;
    }

    public String getUserEvaluation() {
        return userEvaluation;
    }

    public String getReviewTitle() {
        return reviewTitle;
    }

    public String getReviewContent() {
        return reviewContent;
    }

    public String getReviewLikeCount() {
        return reviewLikeCount;
    }

    public String getReviewComments() {
        return reviewComments;
    }
}
